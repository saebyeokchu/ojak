<script>

    function openToggleDivSm(event){
        event.preventDefault();
        const smToggleDiv = document.getElementById('sm-header-toggle-div');

        if(smToggleDiv){
            smToggleDiv.style.transform = "translateX(0vh)"; 
        }
    } 

    function closeToggleDivSm(){
        const toggleDiv = document.getElementById('sm-header-toggle-div');
        if(toggleDiv){
            toggleDiv.style.transform = "translateX(-100vh)"; 
        }
    }

    function makeMobileScrollHeader(){
        console.log("[isScrollHeaderWrapper]");
        const scrollHeader = document.getElementById("mobileScrollHeaderWrapper");
        const blackHeader = document.getElementById("mobileBlackHeaderWrapper");
        const whiteHeader = document.getElementById("mobileWhiteHeaderWrapper");

        blackHeader.style.display = "none";
        whiteHeader.style.display = "none";

        scrollHeader.style.display = "block";
    }

    function makeMobileDynamicHeader(){
        const href = window.location.href;
        const isBlackHeaderPage = href.includes('brand') ||  href.includes('community') || href.includes('gallery');
        const isScrollHeaderPage = href.includes("community/new") || href.includes("community/edit") || href.includes("auth/register-request-complete") || href.includes("contract") || href.includes("personalinfo");

        const scrollHeader = document.getElementById("mobileScrollHeaderWrapper");
        const blackHeader = document.getElementById("mobileBlackHeaderWrapper");
        const whiteHeader = document.getElementById("mobileWhiteHeaderWrapper");

        if(isScrollHeaderPage){
            makeScrollHeader();
        }else if(isBlackHeaderPage){
            console.log("[isBlackHeaderPage]");
            whiteHeader.style.display = "none";
            scrollHeader.style.display = "none";

            blackHeader.style.display = "block";
        }else{
            console.log("[whiteHeader]");
            scrollHeader.style.display = "none";
            blackHeader.style.display = "none";
            
            whiteHeader.style.display = "block";
        }
    }

    document.addEventListener("DOMContentLoaded", () => {
        makeMobileDynamicHeader();

        window.addEventListener("scroll", () => {
            if (window.scrollY > 30) { 
                makeMobileScrollHeader();
            }else{
                makeMobileDynamicHeader();
            }
        });
        
    });

</script>